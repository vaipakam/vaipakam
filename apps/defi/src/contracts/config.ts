function str(key: string, fallback: string): string {
  return (env[key] as string | undefined) ?? fallback;
}

/** Static chain metadata — every field that doesn't change between
 *  deploys lives here. The dynamic fields (Diamond address, deploy
 *  block, facet/adapter addresses) are pulled from the deployments
 *  JSON via `buildChainConfig` below. */
interface ChainMeta {
  chainId: number;
  chainIdHex: string;
  name: string;
  shortName: string;
  rpcUrlEnvKey: string;
  rpcUrlDefault: string;
  blockExplorer: string;
  isCanonicalVPFI: boolean;
  testnet: boolean;
  nativeGasSymbol: string;
  nativeGasCoinGeckoSlug: string | null;
  bridgedWethCoinGeckoSlug: string | null;
  /** OfferBook default-pair pre-fills — see ChainConfig docstrings.
   *  Optional on the meta input so chains without sensible defaults
   *  (testnets with deploy-shifting mocks, local Anvil) can omit them
   *  and the {@link buildChainConfig} pass folds null. */
  wrappedNativeAddress?: string | null;
  /** Optional override for the OfferBook default-collateral address
   *  on chains where wrapped-native ≠ bridged-WETH9 (BNB Chain
   *  mainnet, Polygon PoS mainnet). When null/undefined, consumers
   *  fall back to {wrappedNativeAddress} — correct on ETH-native
   *  chains where the two values are identical. Per the 2026-05-14
   *  WETH chain-safety audit + the user-flagged frontend gap. */
  bridgedWethAddress?: string | null;
  predominantStableAddress?: string | null;
}

/** Chains whose Diamond is deployed + present in the shared deployments
 *  bundle for INDEXING, but which are NOT yet user-facing. The indexer reads
 *  the bundle directly (not this frontend registry), so keeping a chain here
 *  lets it be tracked while staying out of the app: `buildChainConfig` folds
 *  `diamondAddress → null` for these, which drops them from `ChainSwitcher`
 *  (filters `diamondAddress !== null`) and flips `isChainSupported` false so
 *  protocol calls are gated off — a user can't select a half-configured chain.
 *
 *  Currently EMPTY — every deployed chain in the bundle is user-facing.
 *
 *  History: BNB testnet (97) sat here from #859 while its cross-chain stack +
 *  diamond were deployed and indexed but its `ConfigureOracle` follow-up hadn't
 *  run (no `wethContract`/ETH feed), so `OracleFacet` fail-closed every asset to
 *  Illiquid. That follow-up landed in #860 (mock-WETH numeraire + real ETH/USD
 *  feed + PancakeSwap-V3 liquidation adapter) and PartialFlows are indexed on 97,
 *  so it graduated to user-facing. (Note: on the testnet the mock WETH is
 *  unpaired, so only the numeraire is deep-liquid — real BNB-testnet assets still
 *  value/route conservatively; an accepted testnet/mirror limitation, not a
 *  reason to hide the chain.) Add a chainId here when a new chain is in the
 *  bundle for indexing but not yet oracle-configured. (#856/#859 Codex.) */
const INDEXER_ONLY_CHAIN_IDS = new Set<number>([]);

/** Folds a `ChainMeta` + the matching deployments-JSON record into a
 *  full `ChainConfig`. RPC URL still comes from the operator's
 *  `.env.local`; everything else flows from the deployments JSON. */
function buildChainConfig(meta: ChainMeta): ChainConfig {
  const dep: Deployment | undefined = getDeployment(meta.chainId);
  const userFacingDiamond = INDEXER_ONLY_CHAIN_IDS.has(meta.chainId)
    ? null
    : (dep?.diamond ?? null);
  return {
    chainId: meta.chainId,
    chainIdHex: meta.chainIdHex,
    name: meta.name,
    shortName: meta.shortName,
    rpcUrl: str(meta.rpcUrlEnvKey, meta.rpcUrlDefault),
    blockExplorer: meta.blockExplorer,
    diamondAddress: userFacingDiamond,
    deployBlock: dep?.deployBlock ?? 0,
    isCanonicalVPFI: meta.isCanonicalVPFI,
    testnet: meta.testnet,
    metricsFacetAddress: dep?.facets?.metricsFacet ?? null,
    vaultImplAddress: dep?.vaultImpl ?? null,
    riskFacetAddress: dep?.facets?.riskFacet ?? null,
    profileFacetAddress: dep?.facets?.profileFacet ?? null,
    nativeGasSymbol: meta.nativeGasSymbol,
    nativeGasCoinGeckoSlug: meta.nativeGasCoinGeckoSlug,
    bridgedWethCoinGeckoSlug: meta.bridgedWethCoinGeckoSlug,
    wrappedNativeAddress: meta.wrappedNativeAddress ?? null,
    bridgedWethAddress: meta.bridgedWethAddress ?? null,
    predominantStableAddress: meta.predominantStableAddress ?? null,
  };
}

