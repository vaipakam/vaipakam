/**
 * Registry of chains the Vaipakam app is aware of.
 *
 * Phase 1 deploys each Vaipakam Diamond per-chain (no cross-chain protocol
 * state — each network hosts its own independent Diamond instance). Only
 * the VPFI token is cross-chain, via Chainlink CCIP Cross-Chain Token
 * (CCT) pools post-T-068 (2026-05-18): Base hosts the canonical VPFIToken
 * + a CCIP `LockReleaseTokenPool` (lock/release); every other chain
 * hosts a `VPFIMirrorToken` proxy backed by a CCIP `BurnMintTokenPool`.
 * That canonical-vs-mirror split is captured by the `isCanonicalVPFI`
 * flag on each entry.
 *
 * **Source of truth for addresses**: `frontend/src/contracts/deployments.json`,
 * generated by `contracts/script/exportFrontendDeployments.sh` from the
 * canonical per-chain artifacts under `contracts/deployments/<slug>/addresses.json`.
 * After every redeploy, run the export script and the live addresses flow
 * through to the next `vite build`. The static metadata below (chainId,
 * name, blockExplorer, isCanonicalVPFI flag) intentionally stays
 * hand-maintained here — it doesn't change between deploys and is the
 * frontend's authoritative record of what chains the app supports.
 *
 * **What still lives in `.env.local`**: per-chain RPC URLs (the operator's
 * Alchemy/drpc endpoint with API key), WalletConnect project ID, the
 * default chain ID, and a few feature flags. Everything else — Diamond
 * address, vault impl, facet addresses, cross-chain token pool addresses
 * — comes from the consolidated deployments JSON.
 */

